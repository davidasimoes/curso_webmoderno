//APRENDENDO A CRIAR VARIÁVEIS E CONSTANTES

//forma padrão de declarar variáveis
var a = 3;

//outra forma de declarar variáveis (preferível)
let b = 4;

var a = 30;
b = 40;
//variáveis com LET  não podem ser redeclaradas.

a = 300;
b = 400;

//Para declarar constantes
const c = 5;
//c = 5; constantes não podem ter seu valor alterados
console.log(a, b, c);